CREATE TABLE IF NOT EXISTS employee (
id VARCHAR(200) PRIMARY KEY NOT NULL,
first_name VARCHAR(220) NOT NULL,
last_name VARCHAR(200) NOT NULL,
email VARCHAR(240) NOT NULL UNIQUE,
contact VARCHAR(255) NOT NULL,
address VARCHAR(220) NOT NULL,
salary VARCHAR(200) NOT NULL,
join_date VARCHAR(255) NOT NULL,
designation VARCHAR(225) NOT NULL,
depid VARCHAR(200) NULL,
uid VARCHAR(220) NULL,
FOREIGN KEY (depid) REFERENCES department(id) on delete SET null,
FOREIGN KEY (uid) REFERENCES user(id) on delete SET null,
create_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
update_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);